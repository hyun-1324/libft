#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		len;
	int		i;
	char	*str;

	len = 0;
	i = 0;
	len = ft_strlen(src);
	str = (char *)malloc((len + 1) * sizeof(char));
	if (!str)
		return (NULL);
	while (src[i])
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
